                                For how long should you wear sunglasses after laser operation?
                            </a>

                            <p class="accordion-content">When performing LASIK surgery, the surgeon cuts a fold of tissue on the top of the patient’s cornea.
                                Next, the top tissue layer is lifted back, which aids in precisely working upon the cornea’s surface. After
                                the surgery is completed, the surgeon lowers the flap back into its position for the tissues to heal.
                                Though LASIK surgery usually does not cause pain, your eyes could be itchy, dry and sensitive to light and
                                the sun. <br>
                                You must wear your sunglasses to guard the eyes after the surgery, as they dim the harsh sunlight to
                                reduce irritation and sensitivity. In addition, they block ultraviolet rays that hamper the healing process
