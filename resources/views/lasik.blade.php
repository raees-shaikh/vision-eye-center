                                How long does it take to have clear vision after LASIK surgery?
                            </a>

                            <p class="accordion-content">In most cases, patients recover from LASIK surgery six to twelve hours after the surgery. This depends on
                                certain factors, but most patients experience a clearer vision within twenty-four hours after LASIK
                                surgery. Some others would need between two and five days to recover. However, they could also
                                experience vision fluctuations and blurred vision for many weeks after their surgery. <br>
                                As patients have blurry vision immediately after their surgery, we recommend that a family member or
                                friend drive their home after the surgery. Relax and have a nap, but avoid reading or watching television
                                that could stress your eyes. After your procedure, you must return to the clinic to check for vision and
                                take steroid eyedrops and antibiotics for one week. The patient will fully recover after three to six
                                months when the vision finally stabilises.
                            </p>
