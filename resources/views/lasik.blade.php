                                flap and delays the recovery process. <br>
                                You must wear your sunglasses through the recovery processes, especially the first twenty-four hours
                                after LASIK surgery. After a few more days, your eyes would gradually lose their sensitivity, but the
                                patient must wear them when going outdoors in cloudy weather (at least for one month). Even after
                                complete recovery, sunglasses protect the eyes from long-term ultraviolet damage, prevent growth and
                                cataracts.
