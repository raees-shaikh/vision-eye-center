                                Does LASIK surgery impact your eyesight?
                            </a>

                            <p class="accordion-content show">Laser-assisted in situ keratomileusis, abbreviated as LASIK, is a refractive surgery. In this procedure, the
                                surgeon cuts a thin fold of tissue from the front of the patient’s eye. A laser burns away the offending
                                tissue to reshape the cornea, which helps the light focus on the retina at the back of their eye. LASIK
                                correct vision problems such as farsightedness, astigmatism and nearsightedness.   <br>
                                Considered to be an effective and safe procedure, the surgeon operates daily. Most patients have 20/20
                                vision, but people experience side effects such as halos, glare, pain or even loss of sight. Some people
                                have even had 20/40 vision, which is suitable for clearing a driver’s licensing examination. </p>
